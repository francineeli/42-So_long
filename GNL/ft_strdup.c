#include "get_next_line.h"

char	*ft_strdup(const char *s)
{
	size_t		size;
	char		*string;
	int			i;

	size = ft_strlen(s);
	if (!s)
		return (NULL);
	string = malloc(sizeof(char) * (size + 1));
	i = 0;
	while (s[i] != '\0')
	{
		string[i] = s[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}
